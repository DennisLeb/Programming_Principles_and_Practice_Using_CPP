// [2] Write a list of instructions for a computer to find the upstairs bathroom

#include "PPPheaders.h"

static int upstairsBathroom() {

	cout << "Empty your hands. Put everything you were holding on the table in front of you.\n";
	cout << "Stand up from your chair and turn around, towards the staircase.\n";
	cout << "Making sure your path is clear, walk towards the staircase, using your legs.\n";
	cout << "Once arrived at the staircase, place your right foot on the first step and shift your body weight onto that foot.\n";
	cout << "Now place your left foot on the next step and shift your body weight again.\n";
	cout << "Keep doing this and alternate between your feet once for every step on the staircase, advancing your body upwards.\n";
	cout << "Having arrived at the top of the staircase, turn 90 degrees to the right.\n";
	cout << "Walk through the hallway until you reach the bathroom door to your left. Turn 90 degrees to the left.\n";
	cout << "Place a hand of your choosing upon the door handle and firmly press it down, then push the door until it is open.\n";
	cout << "Walk forwards until you have passed the now opened door, then turn by 180 degrees.\n";
	cout << "Again, place a hand of your choosing upon the inner side of the door and push until shut.\n";
	cout << "Turn by 180 degrees. The rest I am unwilling to instruct on. Good luck.\n";
}
